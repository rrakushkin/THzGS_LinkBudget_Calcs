% freq and D can be scalars or vectors, thus loss can be either a scalar, a
% vector, or a 2D array depending on freq and D. 

% D = diameter of dish in meters
% f = frequency of signal in Hz
%offAxisError = pointing error 
%function [HPBW,loss] = pointingLoss(freq, D, offAxisError)
    %c = physconst('lightspeed');
   % HPBW = 70 * (c ./ freq) .* D;
    %loss = 12.*((offAxisError ./ HPBW).^2);
%end
